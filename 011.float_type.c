#include <stdio.h>

int main(void)
{
    // Declare a float type parameter 'ft' then initiate it with 1.234567890123456789.
    float ft = 1.234567890123456789;
    
    // Declare a double type parameter 'ft' then initiate it with 1.234567890123456789.
    double db =  1.234567890123456789;
    
    // Print float type parameter 'ft' up to 20 decimal places.
    printf("The value of the float type parameter: %.20f\n", ft);
    
    //  Print double type parameter 'db' up to 20 decimal places.vvv
    printf("The value of the double type parameter", db);
    
    return 0;
}
